// Helper functions for company data
export const getCompanyById = (id) => {
    return LESOTHO_COMPANIES.find(company => company.id === id)
}

export const getCompanyByEmail = (email) => {
    return LESOTHO_COMPANIES.find(company => company.email === email)
}

export const getCompaniesByIndustry = (industry) => {
    return LESOTHO_COMPANIES.filter(company => company.industry === industry)
}

export const getCompaniesByLocation = (location) => {
    return LESOTHO_COMPANIES.filter(company => company.location === location)
}

export const getAllCompanies = () => {
    return LESOTHO_COMPANIES
}

export const getCompanyLoginCredentials = () => {
    return LESOTHO_COMPANIES.map(company => ({
        name: company.name,
        email: company.email,
        password: company.password,
        location: company.location,
        industry: company.industry
    }))
}
